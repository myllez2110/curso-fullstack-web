const numero1 = 10;
const numero2 = '10';

console.log(numero1 === numero2); // valor e tipo iguais.
console.log(numero1 == numero2); // valor igual, sem checar o tipo.

console.log(numero1 != numero2); // valor diferente, sem checar o tipo
console.log(numero1 !== numero2); // valor e tipo diferente.

console.log(numero1 > numero2);
console.log(numero1 < numero2);
console.log(numero1 >= numero2);
console.log(numero1 <= numero2);
